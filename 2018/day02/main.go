func Do(input string) int {
	twos, threes := 0, 0
	for _, box := range strings.Split(input, "\n") {
		if len(box) == 0 {
			continue
		}
		seen := make(map[rune]int)
		for _, r := range []rune(box) {
			seen[r]++
		}
		for _, c := range seen {
			if c == 2 {
				twos++
				break
			}
		}
		for _, c := range seen {
			if c == 3 {
				threes++
				break
			}
		}
	}
	return twos * threes
}

func Do2(input string) string {
	subs := make(map[string]string)
	for _, box := range strings.Split(input, "\n") {
		for i := range box {
			mod := ""
			switch i {
			case 0:
				mod = box[1:]
			case len(box) - 1:
				mod = box[:len(box)-1]
			default:
				mod = box[:i] + box[i+1:]
			}
			if subs[mod] != "" && box != subs[mod] {
				fmt.Println(mod, box, subs[mod])
				return mod
			}
			subs[mod] = box
		}
	}
	return ""
}

