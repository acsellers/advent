func Calc(ops []int) int {
	current := 0
	for ops[current] != 99 {
		a := ops[current+1]
		b := ops[current+2]
		dest := ops[current+3]

		switch ops[current] {
		case 1:
			ops[dest] = ops[a] + ops[b]
			current += 4
		case 2:
			ops[dest] = ops[a] * ops[b]
			current += 4
		}

	}
	return ops[0]
}
